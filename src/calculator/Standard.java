    public void storeDisplay(ArrayList<Double> numbers, ArrayList<Character> operators){
        if(!display.getText().endsWith("+") &&
           !display.getText().endsWith("-") &&
           !display.getText().endsWith("*") &&
           !display.getText().endsWith("/") &&
           !display.getText().endsWith(".")){
            String[] numbersTemp = display.getText().split("[\\+ \\- * /]");
            for (String number : numbersTemp) {
                numbers.add(Double.parseDouble(number));
            }
            for(int i = 0; i < display.getText().length(); i++){
            if(display.getText().charAt(i) == '+' ||
               display.getText().charAt(i) == '-' ||
               display.getText().charAt(i) == '*' ||
               display.getText().charAt(i) == '/'){
                operators.add(display.getText().charAt(i));
                }
            }
        }
    }
    
    public void calculate(ArrayList<Double> numbers, ArrayList<Character> operators){
        if(numbers.size()-1 == operators.size()){
            double result = 0;
            int i = 0;
            while(i < operators.size()){
                if(operators.isEmpty()){
                    break;
                }
                switch(operators.get(i)){
                    case '*':
                        double multiResult = numbers.get(i)*numbers.get(i+1);
                        numbers.add(i, multiResult);
                        numbers.remove(i+1);
                        numbers.remove(i+1);
                        operators.remove(i);
                        i=0;
                        break;
                    case '/':
                        double divResult = numbers.get(i)/numbers.get(i+1);
                        numbers.add(i, divResult);
                        numbers.remove(i+1);
                        numbers.remove(i+1);
                        operators.remove(i);
                        i=0;
                        break;
                    default: i++;
                }
            }
            i = 0;
            while(i < operators.size()){
                if(operators.isEmpty()){
                    break;
                }
                switch(operators.get(i)){
                    case '+':
                        double addResult = numbers.get(i)+numbers.get(i+1);
                        numbers.add(i, addResult);
                        numbers.remove(i+1);
                        numbers.remove(i+1);
                        operators.remove(i);
                        i=0;
                        break;
                    case '-':
                        double subResult = numbers.get(i)-numbers.get(i+1);
                        numbers.add(i, subResult);
                        numbers.remove(i+1);
                        numbers.remove(i+1);
                        operators.remove(i);
                        i=0;
                        break;
                    default: i++;
                }
            }
            result = numbers.get(0);
            jLabelResult.setText("=" + String.valueOf(result));
        }
    }
    
